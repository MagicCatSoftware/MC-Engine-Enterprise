// Shared shape for the common "type something, add it, see it in a list" pattern —
// GET+POST pipes over a mock DB collection, feeding a loop. Matches the pattern already
// validated in Import-Examples/01-pipes-guestbook.mce.json.
function crudWidget({ collection, placeholder, addLabel, seed, itemPrefix }) {
  const addedEvent = 'added_' + collection;
  const fetchEvent = 'fetch_' + collection;
  const getPipe = 'get_' + collection;
  const addPipe = 'add_' + collection;
  const loopName = 'loop_' + collection;
  return {
    rootOrder: ['M1'],
    machines: {
      M1: { id:'M1', name:'app', tag:'div', text:'', parentId:'', children:['M2','M3','M4'],
        css:{ display:'flex', flexDirection:'column', gap:'8px', padding:'14px', fontFamily:'system-ui,sans-serif', background:'#0d0d1a', borderRadius:'8px' },
        attrs:{}, wires:[
          { eventName:addedEvent, targetId:'', action:'pipeIn',  actionArgs:addPipe },
          { eventName:fetchEvent, targetId:'', action:'pipeOut', actionArgs:getPipe },
        ], viewBinding:null },
      M2: { id:'M2', name:'row', tag:'div', text:'', parentId:'M1', children:['M2a','M2b'],
        css:{ display:'flex', gap:'6px' }, attrs:{}, wires:[], viewBinding:null },
      M2a: { id:'M2a', name:'input', tag:'input', text:'', parentId:'M2', children:[],
        css:{ flex:'1', padding:'7px 10px', borderRadius:'5px', border:'1px solid #333', background:'#06060f', color:'#e2e8f0' },
        attrs:{ type:'text', placeholder: placeholder }, wires:[], viewBinding:null },
      M2b: { id:'M2b', name:'add_btn', tag:'button', text: addLabel, parentId:'M2', children:[],
        css:{ padding:'7px 14px', borderRadius:'5px', border:'none', background:'#a78bfa', color:'#000', fontWeight:'700', cursor:'pointer' },
        attrs:{}, transferOnClick:{ fromId:'M2a', eventName:addedEvent }, wires:[], viewBinding:null },
      M3: { id:'M3', name:'refresh_btn', tag:'button', text:'↻ Refresh', parentId:'M1', children:[],
        css:{ padding:'5px 10px', borderRadius:'5px', border:'1px solid #333', background:'transparent', color:'#888', fontSize:'11px', cursor:'pointer', alignSelf:'flex-start' },
        attrs:{}, emitOnClick:fetchEvent, wires:[], viewBinding:null },
      M4: { id:'M4', name:'list', tag:'div', text:'', parentId:'M1', children:[],
        css:{ display:'flex', flexDirection:'column', gap:'4px' }, attrs:{}, wires:[], viewBinding:null },
    },
    events: { [addedEvent]:{name:addedEvent,payload:'{value}'}, [fetchEvent]:{name:fetchEvent,payload:'{}'} },
    pipes: {
      [getPipe]: { name:getPipe, method:'GET', collection:collection, endpoint:'/api/db/'+collection, delay:0, live:false },
      [addPipe]: { name:addPipe, method:'POST', collection:collection, endpoint:'/api/db/'+collection, delay:0, live:false },
    },
    loops: { [loopName]: { name:loopName, pipeName:getPipe, targetId:'M4', dataField:'', clickEvent:'', clickView:'', clickValueField:'',
      template:'<div style="padding:6px 10px;border:1px solid #222;border-radius:5px;font-size:13px;color:#e2e8f0">' + itemPrefix + '{{value}}</div>' } },
    dbCollections: [ { name:collection, isArray:true, seed:seed } ],
    views: {}, logic: {}, templates: {}, vars: {}, _nextId: 8,
  };
}

